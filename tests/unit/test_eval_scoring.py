"""Scoring + classify_promise tests."""

from __future__ import annotations

import pytest

from evaluation.scoring import (
    MIN_TRADES_FOR_PROMISING,
    ScoreBreakdown,
    classify_promise,
    compute_robustness_score,
)

# Most tests below are about the score / median_pf / ci_lower thresholds; pass
# a generous trade count so those tests aren't accidentally gated on n_oos.
AMPLE_TRADES = MIN_TRADES_FOR_PROMISING * 10


def test_robustness_score_basic():
    # Median PF 1.5; std 0.1 → consistency = 1/(1+0.1) ≈ 0.909
    # 2 params → penalty 0.95**2 ≈ 0.9025
    # significant → 1.0
    bd = compute_robustness_score([1.4, 1.5, 1.6], num_parameters=2, p_value=0.01)
    assert bd.median_pf == pytest.approx(1.5)
    assert bd.consistency_factor == pytest.approx(1 / (1 + 0.0816497), rel=0.01)
    assert bd.parameter_penalty == pytest.approx(0.9025)
    assert bd.significance_factor == 1.0
    assert bd.score > 0


def test_robustness_score_insignificant_halves():
    bd = compute_robustness_score([1.5], num_parameters=0, p_value=0.5)
    assert bd.significance_factor == 0.5


def test_robustness_score_empty():
    bd = compute_robustness_score([], num_parameters=0, p_value=1.0)
    assert bd.score == 0.0


def test_classify_promise_passes_when_all_thresholds_met():
    bd = ScoreBreakdown(median_pf=1.4, consistency_factor=1.0, parameter_penalty=0.9, significance_factor=1.0, score=1.6)
    v = classify_promise(bd, ci_lower=1.1, n_oos_trades_total=AMPLE_TRADES)
    assert v.is_promising
    assert v.failed_conditions == []


def test_classify_promise_fails_when_score_low():
    bd = ScoreBreakdown(median_pf=1.4, consistency_factor=1.0, parameter_penalty=0.9, significance_factor=1.0, score=1.0)
    v = classify_promise(bd, ci_lower=1.1, n_oos_trades_total=AMPLE_TRADES)
    assert not v.is_promising
    assert any(c.name == "score" for c in v.failed_conditions)


def test_classify_promise_fails_when_median_pf_low():
    bd = ScoreBreakdown(median_pf=1.0, consistency_factor=1.0, parameter_penalty=1.0, significance_factor=1.0, score=2.0)
    v = classify_promise(bd, ci_lower=1.1, n_oos_trades_total=AMPLE_TRADES)
    assert not v.is_promising
    assert any(c.name == "median_pf" for c in v.failed_conditions)


def test_classify_promise_fails_when_ci_lower_low():
    bd = ScoreBreakdown(median_pf=1.4, consistency_factor=1.0, parameter_penalty=1.0, significance_factor=1.0, score=2.0)
    v = classify_promise(bd, ci_lower=0.9, n_oos_trades_total=AMPLE_TRADES)
    assert not v.is_promising
    assert any(c.name == "ci_lower" for c in v.failed_conditions)


def test_failed_condition_records_deficit():
    bd = ScoreBreakdown(median_pf=1.0, consistency_factor=1.0, parameter_penalty=1.0, significance_factor=1.0, score=1.0)
    v = classify_promise(bd, ci_lower=0.5, n_oos_trades_total=AMPLE_TRADES)
    score_cond = [c for c in v.failed_conditions if c.name == "score"][0]
    assert score_cond.deficit == pytest.approx(0.5)
    median_cond = [c for c in v.failed_conditions if c.name == "median_pf"][0]
    assert median_cond.deficit == pytest.approx(0.2)
    ci_cond = [c for c in v.failed_conditions if c.name == "ci_lower"][0]
    assert ci_cond.deficit == pytest.approx(0.5)


# ── Minimum-trade-count gate ────────────────────────────────────────────────


def test_min_trades_gate_zscore_bb_reversion_real_case():
    """Regression test for the n=1 / PF=100 / promising=YES bug seen in the
    2026-04-29 sweeps.

    `ZscoreBbReversion` (the same spec hash 4f53cda1...12b945 generated by
    `--archetype mean_reversion --fast` in both sweep #2 and sweep #3) traded
    exactly once, with a single winning trade producing PF=100. Without a
    minimum-trade-count gate, all three legacy thresholds (score>1.5,
    median_pf>1.2, ci_lower>1.0) are trivially passed by a single perfect
    trade and the strategy is marked promising — which is statistical noise,
    not signal. The gate is the minimum floor below which results are
    uninterpretable (DESIGN.md §5.4 / §5.6)."""
    bd = ScoreBreakdown(
        median_pf=100.0,
        consistency_factor=1.0,
        parameter_penalty=1.0,
        significance_factor=1.0,
        score=100.0,
    )
    v = classify_promise(bd, ci_lower=100.0, n_oos_trades_total=1)
    assert not v.is_promising
    gate_failures = [c for c in v.failed_conditions if c.name == "n_oos_trades_total"]
    assert len(gate_failures) == 1
    assert gate_failures[0].actual == 1.0
    assert gate_failures[0].deficit == pytest.approx(MIN_TRADES_FOR_PROMISING - 1)


def test_min_trades_gate_passes_when_count_at_threshold():
    bd = ScoreBreakdown(median_pf=1.4, consistency_factor=1.0, parameter_penalty=0.9, significance_factor=1.0, score=1.6)
    v = classify_promise(bd, ci_lower=1.1, n_oos_trades_total=MIN_TRADES_FOR_PROMISING)
    assert v.is_promising


def test_min_trades_gate_fails_one_below_threshold():
    bd = ScoreBreakdown(median_pf=1.4, consistency_factor=1.0, parameter_penalty=0.9, significance_factor=1.0, score=1.6)
    v = classify_promise(bd, ci_lower=1.1, n_oos_trades_total=MIN_TRADES_FOR_PROMISING - 1)
    assert not v.is_promising
    assert any(c.name == "n_oos_trades_total" for c in v.failed_conditions)


def test_min_trades_threshold_is_configurable():
    bd = ScoreBreakdown(median_pf=1.4, consistency_factor=1.0, parameter_penalty=0.9, significance_factor=1.0, score=1.6)
    # With a custom threshold of 5, n=10 should pass.
    v = classify_promise(bd, ci_lower=1.1, n_oos_trades_total=10, min_trades_threshold=5)
    assert v.is_promising
    # With the same custom threshold, n=4 should fail on the gate alone.
    v2 = classify_promise(bd, ci_lower=1.1, n_oos_trades_total=4, min_trades_threshold=5)
    assert not v2.is_promising
    assert any(c.name == "n_oos_trades_total" for c in v2.failed_conditions)


def test_classify_promise_requires_n_oos_trades_total():
    """The gate must be a required kwarg so a forgetful caller can't silently
    bypass it. This test pins the contract."""
    bd = ScoreBreakdown(median_pf=1.4, consistency_factor=1.0, parameter_penalty=0.9, significance_factor=1.0, score=1.6)
    with pytest.raises(TypeError):
        classify_promise(bd, ci_lower=1.1)  # type: ignore[call-arg]
